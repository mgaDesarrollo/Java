    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
    //SI PRESIONO EL BOTON GUARDAR Y NO HAY NINGUN RADIO BUTTON SELECCIONADO(TCP NI UDP)
    //    
    if(rbtnTCP.isSelected() == false && rbtnUDP.isSelected() == false)
    {
        //MUESTRA UN MENSAJE 
        JOptionPane.showMessageDialog(this, "Seleccione un tipo de dato para ingresar: TCP o UDP");
    }
    else
    {
    try//PROBAR (SI SE ROMPE LO SALTEA)
    {
        //SI EL RADIO BUTTON TCP ESTA SELECCIONADO
        if (rbtnTCP.isSelected()==true)
        {
            //CREA UN PAQUTE TCP, LE DA UN VALOR NULO PORQUE LO ESTOY INICIALIZANDO. Y DECLARO VARIABLES.
            PaqueteTCP T = null;
            int c = 0;
            String di = "";
            int ns = 0;
            int cv = 0;
            //LA VARIABLE C TOMA EL VALOR DEL TXT DATO (QUE ES UN ENTERO)
            c = Integer.parseInt(txtDato.getText());
            //LA VARIABLE DI TOMA EL VALOR DEL TXT DIRECCION (QUE ES UN STRING)
            di = txtDireccionip.getText();
            //LA VARIABLE NS TOMA EL VALOR DEL TXT NUMERO (QUE ES UN ENTERO)
            ns = Integer.parseInt(txtNumeroSecuencia.getText());
            //LA VARIABLE CV TOMA ELVALOR DEL TXT CODIGO VERIFICACION (QUE ES UN ENTERO)
            cv = Integer.parseInt(txtCodigoDeVerificacion.getText());
            //LE PASA TODAS LAS VARIABLES AL CONSTRUCTOR
            T = new PaqueteTCP(c, di, ns, cv);
            CP.agregar(T);//AGREGA EL PAQUETE T A UNA COLECCION DE PAQUETES (CP)
            JOptionPane.showMessageDialog(this, "El paquete TCP fue sido cargado correctamente, sus valores son: " + T.toString());
            jTextArea2.setText(T.toString()); //MUESTRA UN MENSAJE
        
        }
        //SI EL RADIO BUTTON UDP ESTA SELECCIONADO
        if(rbtnUDP.isSelected() == true)
        {
            //CREA UN PAQUTE UDP, LE DA UN VALOR NULO PORQUE LO ESTOY INICIALIZANDO. Y DECLARO VARIABLES.
            PaqueteUDP U =null;
            int co = 0;
            String dip = "";
            int l = 0;
            //LA VARIABLE CO TOMA EL VALOR DEL TXT DATO (QUE ES UN ENTERO)
            co = Integer.parseInt(txtDato.getText());
            //LA VARIABLE DIP TOMA EL VALOR DEL TXT DIRECCION IP (QUE ES UN STRING)
            dip = txtDireccionip.getText();
            //LA VARIABLE l TOMA EL VALOR DEL TXT LONGITUD (QUE ES UN ENTERO)
            l = Integer.parseInt(txtLongitud.getText());
            //LE PASA TODAS LAS VARIABLES AL CONSTRUCTOR
            U = new PaqueteUDP(co, dip, l);
            CP.agregar(U); //AGREGA EL PAQUETE U A UNA COLECCION DE PAQUETES (CP)
            JOptionPane.showMessageDialog(this, "El paquete UDP fue sido cargado correctamente, sus valores son: " + U.toString());
            jTextArea2.setText(U.toString());//MUESTRA UN MENSAJE
        }
    
    }
    catch(Exception e) //SI ENCUENTRA UN ERROR , MUESTRA EL MENSAJE DE ABAJO
    {
        JOptionPane.showMessageDialog(this, "Los Valores ingresados no son correctos");
    }
    
    LimpiarCajas(); //REFRESCA LAS CAJAS
    
    }
        
        
     
    }//GEN-LAST:event_btnGuardarActionPerformed
    
